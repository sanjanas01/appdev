package com.example.vit.kotlinex;

public class Egjava {

        String name;
        int age;
        String add;

        public Egjava(String name, int age, String add) {
            this.name = name;
            this.age = age;
            this.add = add;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }

        public String getPostalAddress() {
            return add;
        }

        public void setPostalAddress(String add) {
            this.add = add;
        }
}
